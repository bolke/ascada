//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#include "Project.h"                                                                    //
#include "Exceptions.h"
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
pr_t pr_ds;                                                                             //project data structure
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
unsigned long tick=0;
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
uint8_t prSetup()
{
  tick=millis();
  prSetGpio(0x0D,GPIO_OUTPUT_HIGH);  
  return EXCEPTION_NONE;
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
uint8_t prLoop()
{  
  if(millis()-tick > 1000)
  {
    prSetGpio(&(pr_ds.gpio[0x0D]),GPIO_OUTPUT_FLIP);
    tick=millis();
  }
  return EXCEPTION_NONE;
}
